    private void LoadBorba() {
        String sql;
        sql = "SELECT b.DatumVrijemeBorbe AS Datum_i_vrijeme, p1.Naziv AS Pokemon_A, p2.Naziv AS Pokemon_B, a.Naziv AS Arena "
                            + "FROM pokemon.borba AS b "
                            + "LEFT JOIN pokemon.pokemon AS p1 ON b.Pokemon1Id = p1.PokemonId "
                            + "LEFT JOIN pokemon.pokemon AS p2 ON b.Pokemon2Id = p2.PokemonId "
                            + "LEFT JOIN pokemon.arena AS a ON b.ArenaId = a.ArenaId";
        
       try{
           pst = conn.prepareStatement(sql);
           rs = pst.executeQuery();
           
           jTable1.setModel(DbUtils.resultSetToTableModel(rs));
           
           rs.close();
           
           rs = pst.executeQuery();
           
           while(rs.next()){
               broj++;
           }
           
           pst.close();
           rs.close();
           
       }catch(Exception e){
           JOptionPane.showMessageDialog(f, e);
           System.out.println(e);
       }
    }
