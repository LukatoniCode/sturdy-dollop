    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql1;
        String sql2;
        
        sql1 = "Select PokemonId, Naziv from pokemon.pokemon";
        sql2 = "Select ArenaId, Naziv from pokemon.arena";
        
        try{
            pst = conn.prepareStatement(sql1);
            rs = pst.executeQuery();
            
            while(rs.next()){
                if(!id_pok.contains(rs.getString(1))){
                jComboBox1.addItem(rs.getString(2));
                jComboBox2.addItem(rs.getString(2));
                id_pok.add(rs.getString(1));
                }
            }
            
            pst.close();
            rs.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(f, e);
            System.out.println(e);
        }
        
        try{
            pst = conn.prepareStatement(sql2);
            rs = pst.executeQuery();
            
            while(rs.next()){
                if(!id_arena.contains(rs.getString(1))){
                jComboBox3.addItem(rs.getString(2));
                id_arena.add(rs.getString(1));
                }
            }
            
            pst.close();
            rs.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(f, e);
            System.out.println(e);
        }
        
        jComboBox1.setSelectedIndex(-1);
        jComboBox2.setSelectedIndex(-1);
        jComboBox3.setSelectedIndex(-1);
        
        jDialog1.setVisible(true);
        jDialog1.setLocationRelativeTo(null);
        
    }//GEN-LAST:event_jButton1ActionPerformed

